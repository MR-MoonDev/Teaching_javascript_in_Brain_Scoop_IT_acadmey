// toFixed(n)	Rounds to n decimal places	(3.14159).toFixed(2) → "3.14"
// toString()	Converts number to string	(123).toString() → "123"
// parseInt(value)	Converts to integer	parseInt("42") → 42
// parseFloat(value)	Converts to float	parseFloat("3.14") → 3.14
// Math.round(num)	Rounds to nearest integer	Math.round(4.6) → 5
// Math.floor(num)	Rounds down	Math.floor(4.9) → 4
// Math.ceil(num)	Rounds up	Math.ceil(4.1) → 5
// Math.random()	Returns a random number (0-1)	Math.random() → 0.6789